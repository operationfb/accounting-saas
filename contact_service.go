// contactToResponse converts a database Contact row (pgx types, UUIDs) into the
// API response shape (plain/optional strings). Reuses nullTextToPtr from
// expense_service.go for the nullable text columns.
func contactToResponse(c contacts.Contact) *ContactResponse {
	return &ContactResponse{
		ID:                              c.ID.String(),
		OrganisationID:                  c.OrganisationID.String(),
		CreatedByUserID:                 c.CreatedByUserID.String(),
		FirstName:                       nullTextToPtr(c.FirstName),
		LastName:                        nullTextToPtr(c.LastName),
		OrganisationName:                nullTextToPtr(c.OrganisationName),
		Email:                           nullTextToPtr(c.Email),
		BillingEmail:                    nullTextToPtr(c.BillingEmail),
		Telephone:                       nullTextToPtr(c.Telephone),
		Mobile:                          nullTextToPtr(c.Mobile),
		AddressLine1:                    nullTextToPtr(c.AddressLine1),
		AddressLine2:                    nullTextToPtr(c.AddressLine2),
		AddressLine3:                    nullTextToPtr(c.AddressLine3),
		Town:                            nullTextToPtr(c.Town),
		Region:                          nullTextToPtr(c.Region),
		Postcode:                        nullTextToPtr(c.Postcode),
		CountryCode:                     c.CountryCode,
		DefaultPaymentTermsDays:         nullInt32ToPtr(c.DefaultPaymentTermsDays),
		UsesContactLevelEmailSettings:   c.UsesContactLevelEmailSettings,
		UsesContactLevelInvoiceSequence: c.UsesContactLevelInvoiceSequence,
		DisplayContactName:              c.DisplayContactName,
		ChargeVAT:                       c.ChargeVat,
		VATRegistrationNumber:           nullTextToPtr(c.VatRegistrationNumber),
		InvoiceLanguage:                 c.InvoiceLanguage,
		BankSortCode:                    nullTextToPtr(c.BankSortCode),
		BankAccountNumber:               nullTextToPtr(c.BankAccountNumber),
		BankRecipientName:               nullTextToPtr(c.BankRecipientName),
		IsActive:                        c.IsActive,
		CreatedAt:                       c.CreatedAt.Time.Format(time.RFC3339),
		UpdatedAt:                       c.UpdatedAt.Time.Format(time.RFC3339),
	}
}

// =============================================================================
// PGX INTEGER HELPERS (0-preserving)
//
// These complement the helpers in expense_service.go. Crucially they DIFFER from
// pgNullInt32 there: pgNullInt32 maps 0→NULL (right for vat_rate_bps, where 0
// means "no rate"), but for default_payment_terms_days a 0 means "Due on Receipt"
// and must be PRESERVED — only a nil pointer becomes NULL.
// =============================================================================

// pgInt32FromPtr converts a *int32 to pgtype.Int4 preserving 0.
// nil pointer → NULL; non-nil pointer → its value (including 0).
func pgInt32FromPtr(n *int32) pgtype.Int4 {
	if n == nil {
		return pgtype.Int4{Valid: false}
	}
	return pgtype.Int4{Int32: *n, Valid: true}
}

// nullInt32ToPtr is the reverse: pgtype.Int4 → *int32.
// NULL → nil; otherwise a pointer to the value (0 is kept, not dropped).
func nullInt32ToPtr(n pgtype.Int4) *int32 {
	if !n.Valid {
		return nil
	}
	v := n.Int32
	return &v
}
