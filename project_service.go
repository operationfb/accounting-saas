func buildCreateProjectParams(orgID, contactID uuid.UUID, req CreateProjectRequest) (projects.CreateProjectParams, error) {
	status, budgetType, billingUnit, err := validateProjectEnums(req.Status, req.BudgetType, req.BillingRateUnit)
	if err != nil {
		return projects.CreateProjectParams{}, err
	}

	billingRatePence, err := poundsStringToInt64(req.BillingRate)
	if err != nil {
		return projects.CreateProjectParams{}, ErrValidation(fmt.Sprintf("billing_rate %q is not a valid amount", req.BillingRate), err)
	}

	budgetHours, budgetDays, budgetMoney, err := resolveBudgetFields(budgetType, req.BudgetHours, req.BudgetDays, req.BudgetMoney)
	if err != nil {
		return projects.CreateProjectParams{}, err
	}

	hoursPerDay, err := parseOptionalHHMM(req.HoursPerDay, "hours_per_day")
	if err != nil {
		return projects.CreateProjectParams{}, err
	}

	startDate, endDate, err := parseOptionalDates(req.StartDate, req.EndDate)
	if err != nil {
		return projects.CreateProjectParams{}, err
	}

	currency := strings.TrimSpace(req.Currency)
	if currency == "" {
		currency = "GBP"
	}

	return projects.CreateProjectParams{
		OrganisationID:         orgID,
		ContactID:              contactID,
		Name:                   req.Name,
		Status:                 status,
		ContractPoNumber:       pgNullText(req.ContractPONumber),
		ProjectInvoiceSequence: req.ProjectInvoiceSequence,
		Currency:               currency,
		BudgetType:             pgNullText(budgetType),
		BudgetHoursMinutes:     budgetHours,
		BudgetDays:             budgetDays,
		BudgetMoneyPence:       budgetMoney,
		HoursPerDayMinutes:     hoursPerDay,
		BillingRatePence:       pgtype.Int8{Int64: billingRatePence, Valid: true},
		BillingRateUnit:        pgNullText(billingUnit),
		BillingRatePlusVat:     req.BillingRatePlusVAT,
		IsIr35:                 req.IsIR35,
		StartDate:              startDate,
		EndDate:                endDate,
		IncludeUnbillableTime:  req.IncludeUnbillableTime,
	}, nil
}

