// validateProjectEnums validates the three enum-like fields against their
// allowed sets (mirrors the CHECK constraints in the schema). Returns
// (status, budgetType *string, billingRateUnit *string, error).
func validateProjectEnums(rawStatus string, rawBudgetType *string, rawBillingUnit *string) (string, *string, *string, error) {
	status := strings.TrimSpace(rawStatus)
	if status == "" {
		status = "active"
	}
	if !validProjectStatus[status] {
		return "", nil, nil, ErrValidation("status must be one of: active, inactive, completed, cancelled", nil)
	}

	var budgetType *string
	if rawBudgetType != nil {
		v := strings.TrimSpace(*rawBudgetType)
		if v != "" {
			if !validBudgetType[v] {
				return "", nil, nil, ErrValidation("budget_type must be one of: hours, days, money", nil)
			}
			budgetType = &v
		}
	}

	var billingUnit *string
	if rawBillingUnit != nil {
		v := strings.TrimSpace(*rawBillingUnit)
		if v != "" {
			if !validBillingRateUnit[v] {
				return "", nil, nil, ErrValidation("billing_rate_unit must be one of: per_hour, per_day", nil)
			}
			billingUnit = &v
		}
	}

	return status, budgetType, billingUnit, nil
}

// resolveBudgetFields sets the correct budget column based on budget_type,
// leaving the others as NULL. Returns (budgetHoursMinutes, budgetDays,
// budgetMoneyPence, error).
func resolveBudgetFields(budgetType *string, hoursStr *string, daysPtr *int32, moneyStr *string) (pgtype.Int4, pgtype.Int4, pgtype.Int8, error) {
	var hoursMinutes pgtype.Int4
	var days pgtype.Int4
	var moneyPence pgtype.Int8

	if budgetType == nil {
		// No budget set — all three columns stay NULL.
		return hoursMinutes, days, moneyPence, nil
	}

	switch *budgetType {
	case "hours":
		if hoursStr == nil || strings.TrimSpace(*hoursStr) == "" {
			return hoursMinutes, days, moneyPence, ErrValidation("budget_hours is required when budget_type is 'hours'", nil)
		}
		mins, err := hhmmToMinutes(*hoursStr)
		if err != nil {
			return hoursMinutes, days, moneyPence, err
		}
		hoursMinutes = pgtype.Int4{Int32: mins, Valid: true}

	case "days":
		if daysPtr == nil {
			return hoursMinutes, days, moneyPence, ErrValidation("budget_days is required when budget_type is 'days'", nil)
		}
		days = pgtype.Int4{Int32: *daysPtr, Valid: true}

	case "money":
		if moneyStr == nil || strings.TrimSpace(*moneyStr) == "" {
			return hoursMinutes, days, moneyPence, ErrValidation("budget_money is required when budget_type is 'money'", nil)
		}
		pence, err := poundsStringToInt64(*moneyStr)
		if err != nil {
			return hoursMinutes, days, moneyPence, ErrValidation(fmt.Sprintf("budget_money %q is not a valid amount", *moneyStr), err)
		}
		moneyPence = pgtype.Int8{Int64: pence, Valid: true}
	}

	return hoursMinutes, days, moneyPence, nil
}

// parseOptionalHHMM parses a nullable HH:MM (or decimal) hours string to
// total minutes as a nullable pgtype.Int4. Returns an error with the fieldName
// in the message so callers get a clear validation error.
func parseOptionalHHMM(s *string, fieldName string) (pgtype.Int4, error) {
	if s == nil || strings.TrimSpace(*s) == "" {
		return pgtype.Int4{Valid: false}, nil
	}
	mins, err := hhmmToMinutes(*s)
	if err != nil {
		return pgtype.Int4{}, ErrValidation(fmt.Sprintf("%s %q is not a valid HH:MM or decimal hours value", fieldName, *s), err)
	}
	return pgtype.Int4{Int32: mins, Valid: true}, nil
}

// parseOptionalDates parses two nullable "YYYY-MM-DD" date strings.
func parseOptionalDates(startStr, endStr *string) (pgtype.Date, pgtype.Date, error) {
	var startDate, endDate pgtype.Date
	if startStr != nil && strings.TrimSpace(*startStr) != "" {
		t, err := time.Parse("2006-01-02", *startStr)
		if err != nil {
			return startDate, endDate, ErrValidation("start_date must be a valid date in YYYY-MM-DD format", err)
		}
		startDate = pgtype.Date{Time: t, Valid: true}
	}
	if endStr != nil && strings.TrimSpace(*endStr) != "" {
		t, err := time.Parse("2006-01-02", *endStr)
		if err != nil {
			return startDate, endDate, ErrValidation("end_date must be a valid date in YYYY-MM-DD format", err)
		}
		endDate = pgtype.Date{Time: t, Valid: true}
	}
	return startDate, endDate, nil
}

