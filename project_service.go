// hhmmToMinutes converts an "H:MM" or decimal hours string to total minutes.
// Examples: "8:00" → 480, "1:30" → 90, "8.5" → 510, "8" → 480.
func hhmmToMinutes(s string) (int32, error) {
	s = strings.TrimSpace(s)
	if strings.Contains(s, ":") {
		parts := strings.SplitN(s, ":", 2)
		h, err1 := strconv.Atoi(strings.TrimSpace(parts[0]))
		m, err2 := strconv.Atoi(strings.TrimSpace(parts[1]))
		if err1 != nil || err2 != nil || h < 0 || m < 0 || m >= 60 {
			return 0, ErrValidation(fmt.Sprintf("%q is not a valid HH:MM value", s), nil)
		}
		return int32(h*60 + m), nil
	}
	// Decimal or integer hours (e.g. "8.5" or "8").
	f, err := strconv.ParseFloat(s, 64)
	if err != nil || f < 0 {
		return 0, ErrValidation(fmt.Sprintf("%q is not a valid hours value", s), nil)
	}
	// Round to nearest minute. Adding 0.5 before truncation gives round-half-up.
	return int32(f*60 + 0.5), nil
}

// minutesToHHMM converts total minutes to "H:MM" display format.
// 480 → "8:00", 90 → "1:30", 0 → "0:00".
func minutesToHHMM(minutes int32) string {
	h := minutes / 60
	m := minutes % 60
	return fmt.Sprintf("%d:%02d", h, m)
}

// poundsStringToInt64 parses a decimal pound string to integer pence (int64).
// "100.00" → 10000, "0" → 0. A blank string is a no-op (0) — billing fields are
// optional here — which is the one convenience the shared kernel doesn't provide,
// so this thin wrapper keeps it and delegates the actual parse/round to
// money.PoundsToMinor (half-up rounding, never float).
func poundsStringToInt64(s string) (int64, error) {
	s = strings.TrimSpace(s)
	if s == "" {
		return 0, nil
	}
	return money.PoundsToMinor(s)
}
