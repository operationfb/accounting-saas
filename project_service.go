// DeleteProject hard-deletes a project row. Safe for now because projects are
// not yet referenced by invoice lines. When that FK is added, switch to soft-
// delete (deleted_at). Any active member may delete (no ownership rule yet).
func (s *ProjectService) DeleteProject(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
) error {
	projectID, err := uuid.Parse(id)
	if err != nil {
		return ErrValidation("id is not a valid UUID", err)
	}
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return err
	}

	// 404 check so we return NotFound rather than a silent no-op.
	if _, err := s.queries.GetProject(ctx, projects.GetProjectParams{
		ID:             projectID,
		OrganisationID: authOrgID,
	}); err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return ErrNotFound("project", id)
		}
		return ErrInternal(err)
	}

	if err := s.queries.DeleteProject(ctx, projects.DeleteProjectParams{
		ID:             projectID,
		OrganisationID: authOrgID,
	}); err != nil {
		return ErrInternal(err)
	}
	return nil
}

