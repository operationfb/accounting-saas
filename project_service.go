// ListProjects returns every project in the caller's organisation, newest
// first. Any active member may list.
func (s *ProjectService) ListProjects(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
) ([]*ProjectResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	rows, err := s.queries.ListProjects(ctx, authOrgID)
	if err != nil {
		return nil, ErrInternal(err)
	}

	out := make([]*ProjectResponse, 0, len(rows))
	for _, row := range rows {
		out = append(out, projectToResponse(row))
	}
	return out, nil
}

