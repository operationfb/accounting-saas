// =============================================================================

// CreateProject validates + normalises the request and inserts one project.
// Any active org member may create a project (like contacts, they are shared
// business data). Single-table INSERT, no transaction needed.
func (s *ProjectService) CreateProject(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	req CreateProjectRequest,
) (*ProjectResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	contactID, err := uuid.Parse(req.ContactID)
	if err != nil {
		return nil, ErrValidation("contact_id is not a valid UUID", err)
	}
	if err := s.validateContact(ctx, contactID, authOrgID); err != nil {
		return nil, err
	}

	params, err := buildCreateProjectParams(authOrgID, contactID, req)
	if err != nil {
		return nil, err
	}

	row, err := s.queries.CreateProject(ctx, params)
	if err != nil {
		return nil, ErrInternal(err)
	}
	return projectToResponse(row), nil
}

// =============================================================================
// GET
// =============================================================================

// GetProject fetches one project by id, scoped to the org. Any active member
// may read a project.
func (s *ProjectService) GetProject(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
) (*ProjectResponse, error) {
	projectID, err := uuid.Parse(id)
	if err != nil {
		return nil, ErrValidation("id is not a valid UUID", err)
	}
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	row, err := s.queries.GetProject(ctx, projects.GetProjectParams{
		ID:             projectID,
		OrganisationID: authOrgID,
	})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, ErrNotFound("project", id)
		}
		return nil, ErrInternal(err)
	}
	return projectToResponse(row), nil
}

