// UpdateProject performs a full update of all editable fields (PUT semantics).
// Any active member may update a project (no per-user ownership rule, unlike
// expenses).
func (s *ProjectService) UpdateProject(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
	req UpdateProjectRequest,
) (*ProjectResponse, error) {
	projectID, err := uuid.Parse(id)
	if err != nil {
		return nil, ErrValidation("id is not a valid UUID", err)
	}
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	// 404 check before doing any further work.
	if _, err := s.queries.GetProject(ctx, projects.GetProjectParams{
		ID:             projectID,
		OrganisationID: authOrgID,
	}); err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, ErrNotFound("project", id)
		}
		return nil, ErrInternal(err)
	}

	contactID, err := uuid.Parse(req.ContactID)
	if err != nil {
		return nil, ErrValidation("contact_id is not a valid UUID", err)
	}
	if err := s.validateContact(ctx, contactID, authOrgID); err != nil {
		return nil, err
	}

	params, err := buildUpdateProjectParams(projectID, authOrgID, contactID, req)
	if err != nil {
		return nil, err
	}

	row, err := s.queries.UpdateProject(ctx, params)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, ErrNotFound("project", id)
		}
		return nil, ErrInternal(err)
	}
	return projectToResponse(row), nil
}

