// projectToResponse converts a database Project row (pgx types) into the API
// response shape (plain strings, human-readable amounts and times).
func projectToResponse(p projects.Project) *ProjectResponse {
	// Budget: only populate the field matching budget_type; leave others nil.
	var budgetType *string
	var budgetHours *string
	var budgetDays *int32
	var budgetMoney *string

	if p.BudgetType.Valid {
		v := p.BudgetType.String
		budgetType = &v
		switch v {
		case "hours":
			if p.BudgetHoursMinutes.Valid {
				s := minutesToHHMM(p.BudgetHoursMinutes.Int32)
				budgetHours = &s
			}
		case "days":
			if p.BudgetDays.Valid {
				d := p.BudgetDays.Int32
				budgetDays = &d
			}
		case "money":
			if p.BudgetMoneyPence.Valid {
				s := money.MinorToPounds(p.BudgetMoneyPence.Int64)
				budgetMoney = &s
			}
		}
	}

	var hoursPerDay *string
	if p.HoursPerDayMinutes.Valid {
		s := minutesToHHMM(p.HoursPerDayMinutes.Int32)
		hoursPerDay = &s
	}

	var billingRateUnit *string
	if p.BillingRateUnit.Valid {
		s := p.BillingRateUnit.String
		billingRateUnit = &s
	}

	// billing_rate is always set (0 if the user didn't enter one).
	billingRate := "0.00"
	if p.BillingRatePence.Valid {
		billingRate = money.MinorToPounds(p.BillingRatePence.Int64)
	}

	var startDate, endDate *string
	if p.StartDate.Valid {
		s := p.StartDate.Time.Format("2006-01-02")
		startDate = &s
	}
	if p.EndDate.Valid {
		s := p.EndDate.Time.Format("2006-01-02")
		endDate = &s
	}

	return &ProjectResponse{
		ID:                     p.ID.String(),
		OrganisationID:         p.OrganisationID.String(),
		ContactID:              p.ContactID.String(),
		Name:                   p.Name,
		Status:                 p.Status,
		ContractPONumber:       nullTextToPtr(p.ContractPoNumber),
		ProjectInvoiceSequence: p.ProjectInvoiceSequence,
		Currency:               p.Currency,
		BudgetType:             budgetType,
		BudgetHours:            budgetHours,
		BudgetDays:             budgetDays,
		BudgetMoney:            budgetMoney,
		HoursPerDay:            hoursPerDay,
		BillingRate:            billingRate,
		BillingRateUnit:        billingRateUnit,
		BillingRatePlusVAT:     p.BillingRatePlusVat,
		IsIR35:                 p.IsIr35,
		StartDate:              startDate,
		EndDate:                endDate,
		IncludeUnbillableTime:  p.IncludeUnbillableTime,
		CreatedAt:              p.CreatedAt.Time.Format(time.RFC3339),
		UpdatedAt:              p.UpdatedAt.Time.Format(time.RFC3339),
	}
}

