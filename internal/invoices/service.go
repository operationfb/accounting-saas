// GetInvoice fetches one invoice (header + line items), org-scoped. Any active
// member may read.
func (s *Service) GetInvoice(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
) (*InvoiceResponse, error) {
	invID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	inv, err := s.queries.GetInvoice(ctx, invoicesdb.GetInvoiceParams{ID: invID, OrganisationID: authOrgID})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, kernel.ErrNotFound("invoice", id)
		}
		return nil, kernel.ErrInternal(err)
	}
	return s.buildDetail(ctx, s.queries, inv)
}

// ListInvoices returns the org's invoices, newest first, WITHOUT line items (the
// list is a lighter summary; fetch one for its lines). Any active member may list.
func (s *Service) ListInvoices(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
) ([]*InvoiceResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	rows, err := s.queries.ListInvoices(ctx, authOrgID)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	out := make([]*InvoiceResponse, 0, len(rows))
	for _, inv := range rows {
		out = append(out, invoiceToResponse(inv))
	}
	return out, nil
}

// =============================================================================
// NEXT REFERENCE (invoice auto-numbering)
// =============================================================================

// NextInvoiceReference returns the suggested reference for a NEW invoice — the
// org's next sequential invoice number, zero-padded (1 → "001"). The create form
// pre-fills it; the user may overwrite it. Any active member may read it.
func (s *Service) NextInvoiceReference(ctx context.Context, authUserID, authOrgID uuid.UUID) (string, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return "", err
	}
	// The suggestion is the greater of (a) the stored counter floor and (b) one more
	// than the highest numeric reference already in use — so it always advances and
	// never re-suggests a taken number, even if the stored counter has drifted below
	// the references actually in use.
	floor, err := s.authQueries.GetNextInvoiceNumber(ctx, authOrgID)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return "", kernel.ErrNotFound("organisation", authOrgID.String())
		}
		return "", kernel.ErrInternal(err)
	}
	maxRef, err := s.queries.MaxNumericInvoiceReference(ctx, authOrgID)
	if err != nil {
		return "", kernel.ErrInternal(err)
	}
	next := floor
	if maxRef+1 > next {
		next = maxRef + 1
	}
	return formatInvoiceNumber(next), nil
}

