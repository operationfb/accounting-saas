// CreateInvoice validates the request, computes the money roll-up, and writes the
// header + lines + totals in one transaction. The owning org and creator come from
// the token. Any active member may create an invoice; it starts as DRAFT.
func (s *Service) CreateInvoice(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	req CreateInvoiceRequest,
) (*InvoiceResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	ref, err := validateReference(req.Reference)
	if err != nil {
		return nil, err
	}

	contactID, err := uuid.Parse(req.ContactID)
	if err != nil {
		return nil, kernel.ErrValidation("contact_id is not a valid UUID", err)
	}
	if err := s.validateContact(ctx, contactID, authOrgID); err != nil {
		return nil, err
	}

	datedOn, err := parseDate("dated_on", req.DatedOn)
	if err != nil {
		return nil, err
	}
	dueOn, err := parseOptionalDate("due_on", req.DueOn)
	if err != nil {
		return nil, err
	}

	lines, net, tax, total, err := buildLines(req.Items)
	if err != nil {
		return nil, err
	}

	var detail *InvoiceResponse
	err = kernel.WithTx(ctx, s.pool, func(tx pgx.Tx) error {
		qtx := s.queries.WithTx(tx)

		inv, err := qtx.CreateInvoice(ctx, invoicesdb.CreateInvoiceParams{
			OrganisationID:  authOrgID,
			CreatedByUserID: authUserID,
			ContactID:       contactID,
			DatedOn:         datedOn,
			DueOn:           dueOn,
			Reference:       pgtype.Text{String: ref, Valid: true},
			Currency:        normaliseCurrency(req.Currency),
		})
		if err != nil {
			if kernel.IsUniqueViolation(err) {
				return kernel.ErrConflict("an invoice with reference " + ref + " already exists")
			}
			return kernel.ErrInternal(err)
		}

		// Raise the org's invoice-counter floor past this number (when the reference
		// is numeric) so the next suggestion always advances and never reuses it.
		if err := raiseInvoiceCounter(ctx, tx, authOrgID, ref); err != nil {
			return err
		}

		if err := insertLines(ctx, qtx, inv.ID, authOrgID, lines); err != nil {
			return err
		}

		updated, err := qtx.UpdateInvoiceTotals(ctx, invoicesdb.UpdateInvoiceTotalsParams{
			ID:                 inv.ID,
			OrganisationID:     authOrgID,
			NetValueMinor:      net,
			SalesTaxValueMinor: tax,
			TotalValueMinor:    total,
		})
		if err != nil {
			return kernel.ErrInternal(err)
		}

		detail, err = s.buildDetail(ctx, qtx, updated)
		return err
	})
	if err != nil {
		return nil, err
	}
	return detail, nil
}

