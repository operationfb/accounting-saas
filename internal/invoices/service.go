// buildDetail fetches an invoice's line items and assembles the full detail
// response. q is either the pooled query set or the tx-scoped one (both *Queries).
func (s *Service) buildDetail(ctx context.Context, q *invoicesdb.Queries, inv invoicesdb.Invoice) (*InvoiceResponse, error) {
	items, err := q.ListInvoiceItems(ctx, invoicesdb.ListInvoiceItemsParams{
		InvoiceID:      inv.ID,
		OrganisationID: inv.OrganisationID,
	})
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	resp := invoiceToResponse(inv)
	resp.Items = make([]InvoiceItemResponse, 0, len(items))
	for _, it := range items {
		ir, err := itemToResponse(it)
		if err != nil {
			return nil, kernel.ErrInternal(err)
		}
		resp.Items = append(resp.Items, ir)
	}
	return resp, nil
}

// buildFXSummary computes the read-only "Currency Gains/Losses" panel for a foreign
// invoice (modelled on FreeAgent's). It revalues the OUTSTANDING (due) foreign amount
// at the invoice's booking rate vs today's stored rate; the difference is the UNREALISED
// gain/loss. Realised gain on payment is deferred, so realized is "0.00" and net ==
// unrealized. Returns (nil, nil) — panel omitted — when there is no usable today rate or
// nothing to revalue (zero total), so a fresh DB without rates doesn't break the read.
// Caller guarantees the invoice is foreign (exchange_rate set) and SENT.
func (s *Service) buildFXSummary(ctx context.Context, q *invoicesdb.Queries, inv invoicesdb.Invoice) (*InvoiceFXSummary, error) {
	totalMinor := inv.TotalValueMinor
	if totalMinor == 0 {
		return nil, nil // nothing to revalue
	}

	org, err := s.authQueries.GetOrganisation(ctx, inv.OrganisationID)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}

	// Today's rate (home per 1 unit of the invoice currency), nearest-prior fallback. No
	// stored rate ⇒ omit the panel (s.rates may be nil if the fxrates module is unwired).
	if s.rates == nil {
		return nil, nil
	}
	todayRate, ok, err := s.rates.RateOnOrBefore(ctx, inv.Currency, time.Now())
	if err != nil {
		return nil, err
	}
	if !ok {
		return nil, nil
	}

	foreignExp, err := s.currencyExp(ctx, q, inv.Currency)
	if err != nil {
		return nil, err
	}
	homeExp, err := s.currencyExp(ctx, q, org.NativeCurrency)
	if err != nil {
		return nil, err
	}

	// We revalue the OUTSTANDING portion only — the paid portion's FX is realised (and
	// deferred). invoiceValue is its home value at the ORIGINAL booking rate, taken as the
	// same proportion of the stored native total (so it matches what was posted); todayValue
	// reconverts the same foreign amount at today's rate. The signed difference is unrealised.
	dueMinor := dueValueMinor(inv)
	invoiceValueMinor := money.Apportion(inv.NativeTotalValueMinor, dueMinor, totalMinor)
	todayValueMinor := money.ConvertMinor(dueMinor, foreignExp, homeExp, todayRate)
	unrealizedMinor := todayValueMinor - invoiceValueMinor

	return &InvoiceFXSummary{
		BaseCurrency: org.NativeCurrency,
		Currency:     inv.Currency,
		InvoiceDate:  inv.DatedOn.Time.Format("2006-01-02"),
		InvoiceRate:  numericToString(inv.ExchangeRate),
		InvoiceValue: money.MinorToPounds(invoiceValueMinor),
		TodayDate:    time.Now().Format("2006-01-02"),
		TodayRate:    todayRate.String(),
		TodayValue:   money.MinorToPounds(todayValueMinor),
		Unrealized:   money.MinorToPounds(unrealizedMinor),
		Realized:     "0.00", // per-payment realised gain is deferred (see BACKLOG)
		Net:          money.MinorToPounds(unrealizedMinor),
	}, nil
}

// invoiceToResponse maps a header row to the API shape (pence→pounds strings,
// stored status + derived display status). It does NOT include line items — callers
// that need the detail use buildDetail.
func invoiceToResponse(inv invoicesdb.Invoice) *InvoiceResponse {
	return &InvoiceResponse{
		ID:              inv.ID.String(),
		OrganisationID:  inv.OrganisationID.String(),
		CreatedByUserID: inv.CreatedByUserID.String(),
		ContactID:       inv.ContactID.String(),
		DatedOn:         inv.DatedOn.Time.Format("2006-01-02"),
		DueOn:           dateToStringPtr(inv.DueOn),
		Reference:       kernel.NullTextToPtr(inv.Reference),
		Currency:        inv.Currency,
		Status:          inv.Status,
		DisplayStatus:   deriveDisplayStatus(inv),
		NetValue:        money.MinorToPounds(inv.NetValueMinor),
		SalesTaxValue:   money.MinorToPounds(inv.SalesTaxValueMinor),
		TotalValue:      money.MinorToPounds(inv.TotalValueMinor),
		PaidValue:       money.MinorToPounds(inv.PaidValueMinor),
		DueValue:        money.MinorToPounds(dueValueMinor(inv)),
		CreatedAt:       inv.CreatedAt.Time.Format(time.RFC3339),
		UpdatedAt:       inv.UpdatedAt.Time.Format(time.RFC3339),
	}
}

// itemToResponse maps a stored line to the API shape, recomputing its net/VAT/total
// from the inputs (the same computeLineMinor used when writing).
func itemToResponse(it invoicesdb.InvoiceItem) (InvoiceItemResponse, error) {
	netMinor, vatMinor, err := computeLineMinor(it.Quantity, it.PriceMinor, it.SalesTaxRateBps)
	if err != nil {
		return InvoiceItemResponse{}, err
	}
	return InvoiceItemResponse{
		ID:            it.ID.String(),
		Position:      it.Position,
		Description:   it.Description,
		Quantity:      normaliseDecimalString(it.Quantity),
		Price:         money.MinorToPounds(it.PriceMinor),
		SalesTaxRate:  money.BpsToPercentString(it.SalesTaxRateBps),
		NetValue:      money.MinorToPounds(netMinor),
		SalesTaxValue: money.MinorToPounds(vatMinor),
		TotalValue:    money.MinorToPounds(netMinor + vatMinor),
	}, nil
}

// deriveDisplayStatus computes the user-facing status from the stored lifecycle +
// money + due date. The stored states pass through; once SENT the presentation
// depends on payment and the due date (see invoices_schema.sql).
func deriveDisplayStatus(inv invoicesdb.Invoice) string {
	switch inv.Status {
	case StatusDraft:
		return "Draft"
	case StatusScheduled:
		return "Scheduled"
	case StatusWrittenOff:
		return "Written off"
	case StatusRefunded:
		return "Refunded"
	case StatusSent:
		switch {
		case inv.TotalValueMinor == 0:
			return "Zero Value"
		case inv.PaidValueMinor > inv.TotalValueMinor:
			return "Overpaid"
		case inv.PaidValueMinor == inv.TotalValueMinor:
			return "Paid"
		case inv.DueOn.Valid && inv.DueOn.Time.Before(todayUTC()):
			return "Overdue"
		default:
			return "Open"
		}
	default:
		return inv.Status
	}
}

