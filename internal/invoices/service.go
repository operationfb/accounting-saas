// Service holds the connection pool, the invoices query set, the auth query set
// used to resolve the caller's membership/role, and the contacts query set used to
// validate an invoice's contact belongs to the same organisation.
type Service struct {
	pool           *pgxpool.Pool
	queries        *invoicesdb.Queries
	authQueries    auth.Querier
	contactQueries contactsdb.Querier
	// vatQueries is the read-only VAT lookup behind the filed-period lock: issuing or
	// reopening an invoice changes whether it counts in its VAT period, so an invoice
	// dated in an already-filed period can't cross the SENT boundary (assertNotFiled).
	vatQueries *vatdb.Queries
	// poster writes the GL journal entry when an invoice is issued (→SENT) and removes
	// it when reopened. Nil-guarded: with no poster wired, the GL is simply not posted.
	poster ledger.Poster
	// rates is the cross-domain FX-rate read seam (internal/fxrates): when a foreign
	// invoice arrives WITHOUT an explicit exchange_rate, nativeAmounts auto-fills it
	// from the stored daily rate for the invoice date. Nil-guarded — with no rates
	// wired, a missing rate on a foreign invoice stays a 422 (the old behaviour).
	rates RateLookup
}

// RateLookup is the read seam over the exchange-rate service. Given a currency and a
// date it returns the rate (HOME units per 1 unit of currency) effective on or before
// that date; the bool is false when no rate is stored. fxrates.Service satisfies this.
type RateLookup interface {
	RateOnOrBefore(ctx context.Context, currency string, on time.Time) (decimal.Decimal, bool, error)
}

// SetPoster wires the general-ledger poster after construction (mirrors the
// expenses.SetPublisher seam). Nil leaves GL posting off.
func (s *Service) SetPoster(p ledger.Poster) { s.poster = p }

// NewService is the constructor, called once in main.go. contactQueries is a
// cross-domain dependency (like authQueries), used to validate contact_id — the
// same pattern projects uses. vatQueries powers the filed-period lock; pass nil to
// disable it (the lock is then off). rates auto-fills a foreign invoice's exchange
// rate; pass nil to disable auto-fill.
func NewService(
	pool *pgxpool.Pool,
	queries *invoicesdb.Queries,
	authQueries auth.Querier,
	contactQueries contactsdb.Querier,
	vatQueries *vatdb.Queries,
	rates RateLookup,
) *Service {
	return &Service{
		pool:           pool,
		queries:        queries,
		authQueries:    authQueries,
		contactQueries: contactQueries,
		vatQueries:     vatQueries,
		rates:          rates,
	}
}

// assertNotFiled refuses the operation (409 conflict) when the given date falls
// inside a FILED VAT period — moving an invoice across the SENT boundary (issue / send
// / reopen / write_off / refund) changes a submitted return's output VAT. Nil-safe:
// with no vat querier wired (or an invalid date), the lock is inactive. Mirrors the
// bills/banking/expenses guard.
func (s *Service) assertNotFiled(ctx context.Context, orgID uuid.UUID, dated pgtype.Date) error {
	if s.vatQueries == nil || !dated.Valid {
		return nil
	}
	locked, err := s.vatQueries.IsDateInFiledPeriod(ctx, vatdb.IsDateInFiledPeriodParams{OrganisationID: orgID, DatedOn: dated})
	if err != nil {
		return kernel.ErrInternal(err)
	}
	if locked {
		return kernel.ErrConflict("this invoice is dated in a VAT period that has been filed, so its status can no longer be changed")
	}
	return nil
}

