// buildDetail fetches an invoice's line items and assembles the full detail
// response. q is either the pooled query set or the tx-scoped one (both *Queries).
func (s *Service) buildDetail(ctx context.Context, q *invoicesdb.Queries, inv invoicesdb.Invoice) (*InvoiceResponse, error) {
	items, err := q.ListInvoiceItems(ctx, invoicesdb.ListInvoiceItemsParams{
		InvoiceID:      inv.ID,
		OrganisationID: inv.OrganisationID,
	})
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	resp := invoiceToResponse(inv)
	resp.Items = make([]InvoiceItemResponse, 0, len(items))
	for _, it := range items {
		ir, err := itemToResponse(it)
		if err != nil {
			return nil, kernel.ErrInternal(err)
		}
		resp.Items = append(resp.Items, ir)
	}
	return resp, nil
}

// invoiceToResponse maps a header row to the API shape (pence→pounds strings,
// stored status + derived display status). It does NOT include line items — callers
// that need the detail use buildDetail.
func invoiceToResponse(inv invoicesdb.Invoice) *InvoiceResponse {
	return &InvoiceResponse{
		ID:              inv.ID.String(),
		OrganisationID:  inv.OrganisationID.String(),
		CreatedByUserID: inv.CreatedByUserID.String(),
		ContactID:       inv.ContactID.String(),
		DatedOn:         inv.DatedOn.Time.Format("2006-01-02"),
		DueOn:           dateToStringPtr(inv.DueOn),
		Reference:       kernel.NullTextToPtr(inv.Reference),
		Currency:        inv.Currency,
		Status:          inv.Status,
		DisplayStatus:   deriveDisplayStatus(inv),
		NetValue:        money.MinorToPounds(inv.NetValueMinor),
		SalesTaxValue:   money.MinorToPounds(inv.SalesTaxValueMinor),
		TotalValue:      money.MinorToPounds(inv.TotalValueMinor),
		PaidValue:       money.MinorToPounds(inv.PaidValueMinor),
		DueValue:        money.MinorToPounds(dueValueMinor(inv)),
		CreatedAt:       inv.CreatedAt.Time.Format(time.RFC3339),
		UpdatedAt:       inv.UpdatedAt.Time.Format(time.RFC3339),
	}
}

// itemToResponse maps a stored line to the API shape, recomputing its net/VAT/total
// from the inputs (the same computeLineMinor used when writing).
func itemToResponse(it invoicesdb.InvoiceItem) (InvoiceItemResponse, error) {
	netMinor, vatMinor, err := computeLineMinor(it.Quantity, it.PriceMinor, it.SalesTaxRateBps)
	if err != nil {
		return InvoiceItemResponse{}, err
	}
	return InvoiceItemResponse{
		ID:            it.ID.String(),
		Position:      it.Position,
		Description:   it.Description,
		Quantity:      normaliseDecimalString(it.Quantity),
		Price:         money.MinorToPounds(it.PriceMinor),
		SalesTaxRate:  money.BpsToPercentString(it.SalesTaxRateBps),
		NetValue:      money.MinorToPounds(netMinor),
		SalesTaxValue: money.MinorToPounds(vatMinor),
		TotalValue:    money.MinorToPounds(netMinor + vatMinor),
	}, nil
}

// deriveDisplayStatus computes the user-facing status from the stored lifecycle +
// money + due date. The stored states pass through; once SENT the presentation
// depends on payment and the due date (see invoices_schema.sql).
func deriveDisplayStatus(inv invoicesdb.Invoice) string {
	switch inv.Status {
	case StatusDraft:
		return "Draft"
	case StatusScheduled:
		return "Scheduled"
	case StatusWrittenOff:
		return "Written off"
	case StatusRefunded:
		return "Refunded"
	case StatusSent:
		switch {
		case inv.TotalValueMinor == 0:
			return "Zero Value"
		case inv.PaidValueMinor > inv.TotalValueMinor:
			return "Overpaid"
		case inv.PaidValueMinor == inv.TotalValueMinor:
			return "Paid"
		case inv.DueOn.Valid && inv.DueOn.Time.Before(todayUTC()):
			return "Overdue"
		default:
			return "Open"
		}
	default:
		return inv.Status
	}
}

