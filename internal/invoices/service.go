// DeleteInvoice soft-deletes an invoice (sets deleted_at). Allowed to the creator
// or an owner/admin; only a DRAFT may be deleted (else 409). Load + guards + write
// run in one transaction.
func (s *Service) DeleteInvoice(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
) error {
	invID, err := uuid.Parse(id)
	if err != nil {
		return kernel.ErrValidation("id is not a valid UUID", err)
	}
	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return err
	}

	return kernel.WithTx(ctx, s.pool, func(tx pgx.Tx) error {
		qtx := s.queries.WithTx(tx)

		existing, err := qtx.GetInvoice(ctx, invoicesdb.GetInvoiceParams{ID: invID, OrganisationID: authOrgID})
		if err != nil {
			if errors.Is(err, pgx.ErrNoRows) {
				return kernel.ErrNotFound("invoice", id)
			}
			return kernel.ErrInternal(err)
		}
		if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
			return kernel.ErrForbidden("you do not have access to this invoice")
		}
		if existing.Status != StatusDraft {
			return kernel.ErrConflict("only draft invoices can be deleted")
		}

		if err := qtx.SoftDeleteInvoice(ctx, invoicesdb.SoftDeleteInvoiceParams{ID: invID, OrganisationID: authOrgID}); err != nil {
			return kernel.ErrInternal(err)
		}
		return nil
	})
}

