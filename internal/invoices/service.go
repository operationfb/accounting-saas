// UpdateInvoice fully replaces an invoice's editable fields and REBUILDS its lines
// (delete-all + re-insert) inside one transaction, then recomputes the totals. The
// load + ownership + DRAFT-only guards run inside the same transaction to close the
// check-then-write race. Allowed to the creator or an owner/admin; only a DRAFT may
// be edited (else 409).
func (s *Service) UpdateInvoice(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
	req UpdateInvoiceRequest,
) (*InvoiceResponse, error) {
	invID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}
	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return nil, err
	}

	// Validate the request up-front (these are 400/422s independent of DB state).
	ref, err := validateReference(req.Reference)
	if err != nil {
		return nil, err
	}
	contactID, err := uuid.Parse(req.ContactID)
	if err != nil {
		return nil, kernel.ErrValidation("contact_id is not a valid UUID", err)
	}
	if err := s.validateContact(ctx, contactID, authOrgID); err != nil {
		return nil, err
	}
	datedOn, err := parseDate("dated_on", req.DatedOn)
	if err != nil {
		return nil, err
	}
	dueOn, err := parseOptionalDate("due_on", req.DueOn)
	if err != nil {
		return nil, err
	}
	lines, net, tax, total, err := buildLines(req.Items)
	if err != nil {
		return nil, err
	}

	var detail *InvoiceResponse
	err = kernel.WithTx(ctx, s.pool, func(tx pgx.Tx) error {
		qtx := s.queries.WithTx(tx)

		existing, err := qtx.GetInvoice(ctx, invoicesdb.GetInvoiceParams{ID: invID, OrganisationID: authOrgID})
		if err != nil {
			if errors.Is(err, pgx.ErrNoRows) {
				return kernel.ErrNotFound("invoice", id)
			}
			return kernel.ErrInternal(err)
		}
		if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
			return kernel.ErrForbidden("you do not have access to this invoice")
		}
		if existing.Status != StatusDraft {
			return kernel.ErrConflict("only draft invoices can be edited")
		}

		if _, err := qtx.UpdateInvoice(ctx, invoicesdb.UpdateInvoiceParams{
			ID:             invID,
			OrganisationID: authOrgID,
			ContactID:      contactID,
			DatedOn:        datedOn,
			DueOn:          dueOn,
			Reference:      pgtype.Text{String: ref, Valid: true},
			Currency:       normaliseCurrency(req.Currency),
		}); err != nil {
			if kernel.IsUniqueViolation(err) {
				return kernel.ErrConflict("an invoice with reference " + ref + " already exists")
			}
			return kernel.ErrInternal(err)
		}

		// Replace the lines: delete all, re-insert the submitted set.
		if err := qtx.DeleteInvoiceItemsForInvoice(ctx, invoicesdb.DeleteInvoiceItemsForInvoiceParams{
			InvoiceID:      invID,
			OrganisationID: authOrgID,
		}); err != nil {
			return kernel.ErrInternal(err)
		}
		if err := insertLines(ctx, qtx, invID, authOrgID, lines); err != nil {
			return err
		}

		rate, nNet, nTax, nTotal, err := s.nativeAmounts(ctx, qtx, authOrgID, normaliseCurrency(req.Currency), req.ExchangeRate, datedOn, net, tax, total)
		if err != nil {
			return err
		}
		updated, err := qtx.UpdateInvoiceTotals(ctx, invoicesdb.UpdateInvoiceTotalsParams{
			ID:                       invID,
			OrganisationID:           authOrgID,
			NetValueMinor:            net,
			SalesTaxValueMinor:       tax,
			TotalValueMinor:          total,
			ExchangeRate:             rate,
			NativeNetValueMinor:      nNet,
			NativeSalesTaxValueMinor: nTax,
			NativeTotalValueMinor:    nTotal,
		})
		if err != nil {
			return kernel.ErrInternal(err)
		}

		detail, err = s.buildDetail(ctx, qtx, updated)
		return err
	})
	if err != nil {
		return nil, err
	}
	return detail, nil
}

