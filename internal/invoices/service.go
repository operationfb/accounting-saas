package invoices

// service.go
// =============================================================================
// Service — the business logic layer for the invoices domain.
//
// An invoice is a sales document an organisation issues to one of its contacts.
// This service owns authorisation, validation, the money roll-up (lines → header
// totals), the status state machine (status.go), and the mapping between the API
// shape (decimal pound strings + percentage strings) and the database shape (pence
// integers + basis points + pgx types).
//
//   HTTP handler (handler.go)
//     ↓
//   Service (this file)
//     ↓
//   *invoicesdb.Queries (db/invoices — generated by sqlc)
//     ↓
//   PostgreSQL
//
// Money rules (see money/money.go):
//   - Amounts are stored as integer minor units (pence), BIGINT (int64), because
//     invoice totals can exceed the int32 ceiling.
//   - Line price is VAT-EXCLUSIVE; line VAT is ADDED ON TOP via money.AddOnVAT
//     (NOT money.ComputeFixedVAT, which extracts VAT from a gross — the expenses
//     direction). Line net = round(quantity × unit price) to whole pence.
//   - The header net/sales_tax/total are the sums of the lines, recomputed on every
//     save; due_value is a generated column (total − paid).
//
// Authorisation mirrors contacts: any active member may create/read/list; update /
// delete / status change require the invoice's creator OR an owner/admin.
// =============================================================================

