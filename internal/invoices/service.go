// ChangeStatus drives the stored lifecycle via an action discriminator (see
// status.go). Allowed to the creator or an owner/admin. An action that is illegal
// from the current status is a 409. Load + guard + write run in one transaction.
func (s *Service) ChangeStatus(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
	action string,
) (*InvoiceResponse, error) {
	invID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}
	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return nil, err
	}

	var detail *InvoiceResponse
	err = kernel.WithTx(ctx, s.pool, func(tx pgx.Tx) error {
		qtx := s.queries.WithTx(tx)

		existing, err := qtx.GetInvoice(ctx, invoicesdb.GetInvoiceParams{ID: invID, OrganisationID: authOrgID})
		if err != nil {
			if errors.Is(err, pgx.ErrNoRows) {
				return kernel.ErrNotFound("invoice", id)
			}
			return kernel.ErrInternal(err)
		}
		if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
			return kernel.ErrForbidden("you do not have access to this invoice")
		}

		target, ok := resolveTransition(action, existing.Status)
		if !ok {
			return kernel.ErrConflict(fmt.Sprintf("cannot %q an invoice in status %s", action, existing.Status))
		}

		// Filed-period lock: the VAT return counts status='SENT' invoices, so any
		// transition INTO or OUT OF SENT changes whether this invoice is in its period
		// (issue/send add it; reopen/write_off/refund remove it). If the invoice is
		// dated in an already-filed period, that would change a submitted return —
		// refuse it. A transition that doesn't touch SENT (e.g. DRAFT→SCHEDULED) is exempt.
		if target == StatusSent || existing.Status == StatusSent {
			if err := s.assertNotFiled(ctx, authOrgID, existing.DatedOn); err != nil {
				return err
			}
		}

		// A SENT invoice with money recorded against it (a bank Invoice Receipt) is a
		// live/part-paid receivable. Reopening to DRAFT is the ONLY route back to editing
		// — and an edit could then drop the total below the amount already paid. So once
		// any payment has landed, block the reopen: the bank receipt(s) must be removed
		// first (via the banking explain panel), which returns paid_value_minor to 0.
		// Other transitions (write_off / refund) are unaffected.
		if target == StatusDraft && existing.PaidValueMinor > 0 {
			return kernel.ErrConflict("cannot reopen an invoice that has payments recorded against it — remove the bank receipt(s) first")
		}

		updated, err := qtx.UpdateInvoiceStatus(ctx, invoicesdb.UpdateInvoiceStatusParams{
			ID:             invID,
			OrganisationID: authOrgID,
			Status:         target,
		})
		if err != nil {
			return kernel.ErrInternal(err)
		}

		// GL: post the receivable when the invoice is issued (→SENT); remove it when it
		// leaves SENT (reopen / write_off / refund). Atomic with the status change.
		if s.poster != nil {
			switch {
			case target == StatusSent:
				if err := s.postInvoiceSent(ctx, tx, authUserID, authOrgID, updated); err != nil {
					return err
				}
			case existing.Status == StatusSent:
				if err := s.poster.RemoveEntry(ctx, tx, authOrgID, ledgerSourceInvoice, updated.ID); err != nil {
					return err
				}
				// Reopen / write-off / refund is an UNDO, not a settlement: drop any unrealised
				// FX revaluation entry too, so a no-longer-SENT invoice carries no FX history.
				if err := s.poster.RemoveEntry(ctx, tx, authOrgID, ledgerSourceInvoiceRevaluation, updated.ID); err != nil {
					return err
				}
			}
		}

		detail, err = s.buildDetail(ctx, qtx, updated)
		return err
	})
	if err != nil {
		return nil, err
	}
	return detail, nil
}

