// parseDate parses a required YYYY-MM-DD string into a pgtype.Date. field names the
// JSON field for the error message.
func parseDate(field, s string) (pgtype.Date, error) {
	t, err := time.Parse("2006-01-02", strings.TrimSpace(s))
	if err != nil {
		return pgtype.Date{}, kernel.ErrValidation(field+" must be a valid date in YYYY-MM-DD format", err)
	}
	return pgtype.Date{Time: t, Valid: true}, nil
}

// parseOptionalDate parses an optional YYYY-MM-DD pointer; nil/blank → SQL NULL.
func parseOptionalDate(field string, s *string) (pgtype.Date, error) {
	if s == nil || strings.TrimSpace(*s) == "" {
		return pgtype.Date{}, nil
	}
	return parseDate(field, *s)
}

// normaliseCurrency upper-cases the currency and defaults a blank to GBP.
func normaliseCurrency(c string) string {
	c = strings.ToUpper(strings.TrimSpace(c))
	if c == "" {
		return "GBP"
	}
	return c
}

// validateReference trims the (now required) invoice reference and rejects an empty
// / whitespace-only value (422). The handler's `required` binding already rejects a
// missing/empty field as a 400; this guards a whitespace-only one and the
// service-direct callers.
func validateReference(raw string) (string, error) {
	ref := strings.TrimSpace(raw)
	if ref == "" {
		return "", kernel.ErrValidation("reference is required", nil)
	}
	return ref, nil
}

// formatInvoiceNumber renders the org's invoice counter as a zero-padded reference:
// 1 → "001", 42 → "042", 1000 → "1000" (minimum width 3).
func formatInvoiceNumber(n int32) string {
	if n < 1 {
		n = 1
	}
	return fmt.Sprintf("%03d", n)
}

// raiseInvoiceCounter advances the org's invoice-counter floor to one PAST the
// just-used numeric reference, so the next suggestion stays monotonic — a number is
// never re-suggested once used, even after the invoice is deleted. It parses the
// reference as an integer ("001" → 1); a non-numeric / custom reference leaves the
// counter alone. GREATEST (in RaiseInvoiceNumber) only ever moves the floor UP, so
// this is safe to call unconditionally and under concurrency.
func raiseInvoiceCounter(ctx context.Context, tx pgx.Tx, orgID uuid.UUID, reference string) error {
	n, err := strconv.Atoi(strings.TrimSpace(reference))
	if err != nil || n < 1 {
		return nil // custom / out-of-sequence reference — don't move the counter
	}
	if err := auth.New(tx).RaiseInvoiceNumber(ctx, auth.RaiseInvoiceNumberParams{
		ID:      orgID,
		AtLeast: int32(n) + 1,
	}); err != nil {
		return kernel.ErrInternal(err)
	}
	return nil
}

// normaliseDecimalString trims trailing zeros from a numeric string for display
// ("10.0000" → "10", "2.5000" → "2.5"). Falls back to the input if it can't parse.
func normaliseDecimalString(s string) string {
	d, err := decimal.NewFromString(strings.TrimSpace(s))
	if err != nil {
		return s
	}
	return d.String()
}

// numericToString renders a stored pgtype.Numeric (e.g. the invoice's exchange_rate) as
// a clean decimal string with trailing zeros trimmed ("0.867880" → "0.86788"). An invalid
// numeric yields "" (the panel's caller only passes a .Valid rate). Goes via the driver
// Value() (a string) then shopspring/decimal so the format matches the rest of the module.
func numericToString(n pgtype.Numeric) string {
	if !n.Valid {
		return ""
	}
	v, err := n.Value()
	if err != nil {
		return ""
	}
	s, ok := v.(string)
	if !ok {
		return ""
	}
	d, err := decimal.NewFromString(s)
	if err != nil {
		return s
	}
	return d.String()
}

// dateToStringPtr renders a nullable date as a YYYY-MM-DD *string; nil when NULL.
func dateToStringPtr(d pgtype.Date) *string {
	if !d.Valid {
		return nil
	}
	s := d.Time.Format("2006-01-02")
	return &s
}

// dueValueMinor returns the outstanding amount. due_value_minor is a generated
// column (read-only, nullable in Go as pgtype.Int8); fall back to total − paid if
// the driver didn't populate it.
func dueValueMinor(inv invoicesdb.Invoice) int64 {
	if inv.DueValueMinor.Valid {
		return inv.DueValueMinor.Int64
	}
	return inv.TotalValueMinor - inv.PaidValueMinor
}

// todayUTC is midnight today in UTC — the cutoff for "overdue" (an invoice due
// strictly before today is overdue; due today is not).
func todayUTC() time.Time {
	now := time.Now().UTC()
	return time.Date(now.Year(), now.Month(), now.Day(), 0, 0, 0, 0, time.UTC)
}
