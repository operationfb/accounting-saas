// lineCalc is a validated, ready-to-store line plus the computed amounts that feed
// the header totals. quantity is kept as a normalised decimal string for the NUMERIC
// column; priceMinor / rateBps are the stored integer forms.
type lineCalc struct {
	description string
	quantity    string
	priceMinor  int64
	rateBps     int32
}

// buildLines validates each request line, converts pounds→pence and percent→bps,
// computes the line net (quantity × unit price, rounded to whole pence) and line VAT
// (added on top), and returns the lines plus the header net / tax / total. An empty
// item list is allowed (a zero-value draft) and yields zero totals.
func buildLines(items []InvoiceItemRequest) (lines []lineCalc, net, tax, total int64, err error) {
	lines = make([]lineCalc, 0, len(items))
	for i, it := range items {
		n := i + 1 // 1-based for human-friendly error messages

		desc := strings.TrimSpace(it.Description)
		if desc == "" {
			return nil, 0, 0, 0, kernel.ErrValidation(fmt.Sprintf("item %d: description is required", n), nil)
		}
		priceMinor, err := money.PoundsToMinor(strings.TrimSpace(it.Price))
		if err != nil {
			return nil, 0, 0, 0, kernel.ErrValidation(fmt.Sprintf("item %d: price is not a valid amount", n), err)
		}
		rateBps, err := money.PercentToBps(it.SalesTaxRate)
		if err != nil {
			return nil, 0, 0, 0, kernel.ErrValidation(fmt.Sprintf("item %d: sales_tax_rate is not a valid percentage", n), err)
		}
		lineNet, lineTax, err := computeLineMinor(it.Quantity, priceMinor, rateBps)
		if err != nil {
			return nil, 0, 0, 0, kernel.ErrValidation(fmt.Sprintf("item %d: quantity is not a valid number", n), err)
		}

		net += lineNet
		tax += lineTax
		lines = append(lines, lineCalc{
			description: desc,
			quantity:    normaliseDecimalString(it.Quantity),
			priceMinor:  priceMinor,
			rateBps:     rateBps,
		})
	}
	return lines, net, tax, net + tax, nil
}

// computeLineMinor derives a line's net and VAT (in pence) from its stored inputs.
// SINGLE source of truth, used both when writing (buildLines) and when reading
// (itemToResponse), so a line's amounts are computed identically everywhere.
// net = round(quantity × unit price) to whole pence; VAT is added on top.
func computeLineMinor(quantity string, priceMinor int64, rateBps int32) (netMinor, vatMinor int64, err error) {
	qty, err := decimal.NewFromString(strings.TrimSpace(quantity))
	if err != nil {
		return 0, 0, err
	}
	netMinor = qty.Mul(decimal.NewFromInt(priceMinor)).Round(0).IntPart()
	vatMinor = money.AddOnVAT(netMinor, rateBps)
	return netMinor, vatMinor, nil
}

// insertLines writes the validated lines for an invoice, numbering position from 1.
func insertLines(ctx context.Context, q *invoicesdb.Queries, invoiceID, orgID uuid.UUID, lines []lineCalc) error {
	for i, ln := range lines {
		if _, err := q.CreateInvoiceItem(ctx, invoicesdb.CreateInvoiceItemParams{
			InvoiceID:       invoiceID,
			OrganisationID:  orgID,
			Position:        int32(i + 1),
			Description:     ln.description,
			Quantity:        ln.quantity,
			PriceMinor:      ln.priceMinor,
			SalesTaxRateBps: ln.rateBps,
		}); err != nil {
			return kernel.ErrInternal(err)
		}
	}
	return nil
}

