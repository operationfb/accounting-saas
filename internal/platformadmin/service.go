// ListUsers returns every live user on the platform. Superuser only.
func (s *Service) ListUsers(ctx context.Context, authUserID uuid.UUID) ([]AdminUserResponse, error) {
	if err := s.requireSuperuser(ctx, authUserID); err != nil {
		return nil, err
	}
	rows, err := s.authQueries.ListAllUsers(ctx)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	out := make([]AdminUserResponse, 0, len(rows))
	for _, r := range rows {
		out = append(out, AdminUserResponse{
			ID:          r.ID.String(),
			Email:       r.Email,
			FirstName:   r.FirstName,
			LastName:    r.LastName,
			IsActive:    r.IsActive,
			IsSuperuser: r.IsSuperuser,
			LastLoginAt: kernel.TimestampToStringPtr(r.LastLoginAt),
			CreatedAt:   r.CreatedAt.Time.Format(time.RFC3339),
		})
	}
	return out, nil
}

// GetUser returns one user plus every organisation they belong to (the user
// drill-in). Superuser only.
func (s *Service) GetUser(ctx context.Context, authUserID, targetID uuid.UUID) (*AdminUserDetailResponse, error) {
	if err := s.requireSuperuser(ctx, authUserID); err != nil {
		return nil, err
	}
	u, err := s.authQueries.GetUser(ctx, targetID)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, kernel.ErrNotFound("user", targetID.String())
		}
		return nil, kernel.ErrInternal(err)
	}
	memberships, err := s.authQueries.ListMembershipsForUser(ctx, targetID)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	membershipDTOs := make([]AdminMembershipResponse, 0, len(memberships))
	for _, m := range memberships {
		membershipDTOs = append(membershipDTOs, AdminMembershipResponse{
			OrganisationID:   m.OrganisationID.String(),
			OrganisationName: m.OrganisationName,
			Role:             string(m.Role),
			Status:           m.Status,
			MemberSince:      m.MemberSince.Time.Format(time.RFC3339),
		})
	}
	return &AdminUserDetailResponse{
		User: AdminUserResponse{
			ID:          u.ID.String(),
			Email:       u.Email,
			FirstName:   u.FirstName,
			LastName:    u.LastName,
			IsActive:    u.IsActive,
			IsSuperuser: u.IsSuperuser,
			LastLoginAt: kernel.TimestampToStringPtr(u.LastLoginAt),
			CreatedAt:   u.CreatedAt.Time.Format(time.RFC3339),
		},
		Memberships: membershipDTOs,
	}, nil
}
