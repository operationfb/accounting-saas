// DeleteBill soft-deletes a bill. Allowed to the creator or an owner/admin; only an
// UNPAID bill may be deleted (else 409). Load + guards + write run in one transaction.
func (s *Service) DeleteBill(ctx context.Context, authUserID, authOrgID uuid.UUID, id string) error {
	billID, err := uuid.Parse(id)
	if err != nil {
		return kernel.ErrValidation("id is not a valid UUID", err)
	}
	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return err
	}

	return kernel.WithTx(ctx, s.pool, func(tx pgx.Tx) error {
		qtx := s.queries.WithTx(tx)

		existing, err := qtx.GetBill(ctx, billsdb.GetBillParams{ID: billID, OrganisationID: authOrgID})
		if err != nil {
			if errors.Is(err, pgx.ErrNoRows) {
				return kernel.ErrNotFound("bill", id)
			}
			return kernel.ErrInternal(err)
		}
		if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
			return kernel.ErrForbidden("you do not have access to this bill")
		}
		if existing.PaidValueMinor != 0 {
			return kernel.ErrConflict("a bill that has payments recorded against it cannot be deleted — remove the bank payment(s) first")
		}
		if err := s.assertNotFiled(ctx, authOrgID, existing.DatedOn); err != nil {
			return err
		}

		if err := qtx.SoftDeleteBill(ctx, billsdb.SoftDeleteBillParams{ID: billID, OrganisationID: authOrgID}); err != nil {
			return kernel.ErrInternal(err)
		}
		return nil
	})
}

