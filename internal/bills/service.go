package bills

// service.go
// =============================================================================
// Service — the business logic layer for the bills domain.
//
// A bill is an accounts-PAYABLE record: a supplier's invoice the organisation
// owes. This service owns authorisation, validation, the VAT resolution and the
// mapping between the API shape (decimal pound strings) and the database shape
// (pence integers + pgx types). It is the payable twin of internal/invoices, but
// simpler: ONE flat spending line (no line items), NO auto-numbering, and NO
// status lifecycle.
//
//   HTTP handler (handler.go)
//     ↓
//   Service (this file)
//     ↓
//   *billsdb.Queries (db/bills — generated by sqlc)
//     ↓
//   PostgreSQL
//
// VAT (the EXPENSES pattern, see resolveVAT): the entered amount is the
// VAT-INCLUSIVE total. A fixed-ratio vat_rate has its VAT EXTRACTED from the total
// (money.ExtractVAT, the int64 twin of ComputeFixedVAT); a non-fixed-ratio
// ("manual") rate takes the VAT from the client's vat_amount. net = total − VAT.
//
// Lifecycle: none. A bill is editable/deletable only while UNPAID
// (paid_value_minor == 0); paid_value_minor is written by the banking module.
// Authorisation mirrors invoices/contacts: any active member may create/read/list;
// update/delete require the bill's creator OR an owner/admin.
// =============================================================================

