// GetBill fetches one bill, org-scoped. Any active member may read.
func (s *Service) GetBill(ctx context.Context, authUserID, authOrgID uuid.UUID, id string) (*BillResponse, error) {
	billID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}
	bill, err := s.queries.GetBill(ctx, billsdb.GetBillParams{ID: billID, OrganisationID: authOrgID})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, kernel.ErrNotFound("bill", id)
		}
		return nil, kernel.ErrInternal(err)
	}
	return billToResponse(bill), nil
}

// ListBills returns the org's bills, newest first. Any active member may list.
func (s *Service) ListBills(ctx context.Context, authUserID, authOrgID uuid.UUID) ([]*BillResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}
	rows, err := s.queries.ListBills(ctx, authOrgID)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	out := make([]*BillResponse, 0, len(rows))
	for _, b := range rows {
		out = append(out, billToResponse(b))
	}
	return out, nil
}

