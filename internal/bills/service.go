// authorize confirms the caller is an ACTIVE member of the organisation and
// returns their role (used to gate who may modify a bill).
func (s *Service) authorize(ctx context.Context, userID, orgID uuid.UUID) (auth.OrganisationRole, error) {
	return kernel.AuthorizeMember(ctx, s.authQueries, userID, orgID)
}

// validateContact confirms the supplier contact exists in the org (and isn't
// soft-deleted). GetContact already filters by org + deleted_at IS NULL.
func (s *Service) validateContact(ctx context.Context, contactID, orgID uuid.UUID) error {
	_, err := s.contactQueries.GetContact(ctx, contactsdb.GetContactParams{ID: contactID, OrganisationID: orgID})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return kernel.ErrValidation("contact_id does not exist or does not belong to this organisation", nil)
		}
		return kernel.ErrInternal(err)
	}
	return nil
}

// validateCategory confirms category_id is a SPENDING account in the org — the same
// filter ListBillCategories applies to the picker, enforced here at write time so a
// bill can never point at an income / balance-sheet / system-managed account.
func (s *Service) validateCategory(ctx context.Context, categoryID, orgID uuid.UUID) error {
	cat, err := s.categoryQueries.GetCategory(ctx, categoriesdb.GetCategoryParams{ID: categoryID, OrganisationID: orgID})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return kernel.ErrValidation("category_id does not exist or does not belong to this organisation", nil)
		}
		return kernel.ErrInternal(err)
	}
	if !cat.IsActive || cat.IsSystemManaged || !isSpendingAccount(cat.AccountType) {
		return kernel.ErrValidation("category_id is not a valid spending category", nil)
	}
	return nil
}

// resolveProject validates the optional project link and returns it as a pgtype.UUID
// (NULL when no project). A project_id that isn't in the caller's org is a 422.
func (s *Service) resolveProject(ctx context.Context, projectID *string, orgID uuid.UUID) (pgtype.UUID, error) {
	if projectID == nil || strings.TrimSpace(*projectID) == "" {
		return pgtype.UUID{}, nil
	}
	pid, err := uuid.Parse(strings.TrimSpace(*projectID))
	if err != nil {
		return pgtype.UUID{}, kernel.ErrValidation("project_id is not a valid UUID", err)
	}
	if _, err := s.projectQueries.GetProject(ctx, projectsdb.GetProjectParams{ID: pid, OrganisationID: orgID}); err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return pgtype.UUID{}, kernel.ErrValidation("project_id does not exist or does not belong to this organisation", nil)
		}
		return pgtype.UUID{}, kernel.ErrInternal(err)
	}
	return pgUUID(pid), nil
}

// vatResult is the resolved VAT, ready to store on the bill row.
type vatResult struct {
	RateID     pgtype.UUID
	RateBps    pgtype.Int4
	ValueMinor int64
}

// resolveVAT turns the request's vat_rate_id (+ optional vat_amount) into the stored
// VAT fields — the EXPENSES pattern (see internal/expenses resolveVAT), but int64
// for BIGINT bill totals:
//   - no rate selected     → no VAT (NULL rate id/bps, value 0)
//   - fixed-ratio rate     → VAT EXTRACTED from the VAT-inclusive total
//     (money.ExtractVAT); any client amount is ignored (not an error)
//   - non-fixed-ratio rate → value = the client-supplied amount (0 if omitted)
//
// The selected rate must belong to the caller's organisation's country (the
// frontend only offers rates for that country); a mismatch is a validation error.
func (s *Service) resolveVAT(ctx context.Context, authOrgID uuid.UUID, grossMinor int64, vatRateID, vatAmount *string) (vatResult, error) {
	if vatRateID == nil || strings.TrimSpace(*vatRateID) == "" {
		return vatResult{}, nil
	}

	rateUUID, err := uuid.Parse(strings.TrimSpace(*vatRateID))
	if err != nil {
		return vatResult{}, kernel.ErrValidation("vat_rate_id is not a valid UUID", err)
	}

	rate, err := s.categoryQueries.GetVatRate(ctx, rateUUID)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return vatResult{}, kernel.ErrValidation("vat_rate_id not found", nil)
		}
		return vatResult{}, kernel.ErrInternal(err)
	}

	// The rate must be for the caller's organisation's country. The token carries
	// only the org id; the country_code lives on the organisation (auth domain).
	org, err := s.authQueries.GetOrganisation(ctx, authOrgID)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return vatResult{}, kernel.ErrNotFound("organisation", authOrgID.String())
		}
		return vatResult{}, kernel.ErrInternal(err)
	}
	if rate.CountryCode != org.CountryCode {
		return vatResult{}, kernel.ErrValidation("selected VAT rate is not valid for your organisation's country", nil)
	}

	var valueMinor int64
	if rate.IsFixedRatio {
		// Rate-locked: extract the VAT from the inclusive total; ignore any client amount.
		valueMinor = money.ExtractVAT(grossMinor, rate.RateBps)
	} else if vatAmount != nil {
		// Manual rate: accept the client's amount as-is (pounds → pence).
		minor, err := money.PoundsToMinor(strings.TrimSpace(*vatAmount))
		if err != nil {
			return vatResult{}, kernel.ErrValidation(fmt.Sprintf("vat_amount %q is not a valid decimal number", *vatAmount), err)
		}
		valueMinor = minor
	}

	return vatResult{
		RateID:     pgUUID(rateUUID),
		RateBps:    pgtype.Int4{Int32: rate.RateBps, Valid: true},
		ValueMinor: valueMinor,
	}, nil
}

// billInput is the validated, ready-to-store result of vetting a create/update body.
type billInput struct {
	contactID  uuid.UUID
	categoryID uuid.UUID
	projectID  pgtype.UUID
	reference  string
	datedOn    pgtype.Date
	dueOn      pgtype.Date
	currency   string
	comments   pgtype.Text
	hp         bool
	vat        vatResult
	net        int64
	total      int64
}

// vet validates every field of a create/update request (shared so the rules live in
// one place). All errors here are 400/422s independent of which bill is being saved.
func (s *Service) vet(ctx context.Context, authOrgID uuid.UUID, contactID, reference, datedOn string, dueOn *string, currency string, comments *string, hp bool, categoryID, total string, vatRateID, vatAmount, projectID *string) (billInput, error) {
	var in billInput

	ref, err := validateReference(reference)
	if err != nil {
		return in, err
	}
	in.reference = ref

	in.contactID, err = uuid.Parse(contactID)
	if err != nil {
		return in, kernel.ErrValidation("contact_id is not a valid UUID", err)
	}
	if err := s.validateContact(ctx, in.contactID, authOrgID); err != nil {
		return in, err
	}

	in.categoryID, err = uuid.Parse(categoryID)
	if err != nil {
		return in, kernel.ErrValidation("category_id is not a valid UUID", err)
	}
	if err := s.validateCategory(ctx, in.categoryID, authOrgID); err != nil {
		return in, err
	}

	in.projectID, err = s.resolveProject(ctx, projectID, authOrgID)
	if err != nil {
		return in, err
	}

	in.datedOn, err = parseDate("dated_on", datedOn)
	if err != nil {
		return in, err
	}
	in.dueOn, err = parseOptionalDate("due_on", dueOn)
	if err != nil {
		return in, err
	}

	in.total, err = money.PoundsToMinor(strings.TrimSpace(total))
	if err != nil {
		return in, kernel.ErrValidation("total is not a valid amount", err)
	}

	in.vat, err = s.resolveVAT(ctx, authOrgID, in.total, vatRateID, vatAmount)
	if err != nil {
		return in, err
	}
	in.net = in.total - in.vat.ValueMinor

	in.currency = normaliseCurrency(currency)
	in.comments = kernel.NullText(comments)
	in.hp = hp
	return in, nil
}

