// billToResponse maps a bill row to the API shape (pence→pounds strings, derived
// display status).
func billToResponse(b billsdb.Bill) *BillResponse {
	return &BillResponse{
		ID:              b.ID.String(),
		OrganisationID:  b.OrganisationID.String(),
		CreatedByUserID: b.CreatedByUserID.String(),
		ContactID:       b.ContactID.String(),
		Reference:       kernel.NullTextToPtr(b.Reference),
		DatedOn:         b.DatedOn.Time.Format("2006-01-02"),
		DueOn:           dateToStringPtr(b.DueOn),
		Currency:        b.Currency,
		Comments:        kernel.NullTextToPtr(b.Comments),
		IsHirePurchase:  b.IsHirePurchase,
		CategoryID:      b.CategoryID.String(),
		VATRateID:       uuidToStringPtr(b.VatRateID),
		VATRate:         vatRateDisplay(b.VatRateBps),
		NetValue:        money.MinorToPounds(b.NetValueMinor),
		SalesTaxValue:   money.MinorToPounds(b.SalesTaxValueMinor),
		TotalValue:      money.MinorToPounds(b.TotalValueMinor),
		PaidValue:       money.MinorToPounds(b.PaidValueMinor),
		DueValue:        money.MinorToPounds(dueValueMinor(b)),
		DisplayStatus:   deriveDisplayStatus(b),
		ProjectID:       uuidToStringPtr(b.ProjectID),
		CreatedAt:       b.CreatedAt.Time.Format(time.RFC3339),
		UpdatedAt:       b.UpdatedAt.Time.Format(time.RFC3339),
	}
}

// deriveDisplayStatus computes the user-facing label from money + due date. There is
// no stored status; this is derived at read time so it never goes stale. paid is
// written by the banking module.
func deriveDisplayStatus(b billsdb.Bill) string {
	total := b.TotalValueMinor
	paid := b.PaidValueMinor
	switch {
	case total == 0:
		return "Zero Value"
	case paid >= total:
		return "Paid"
	case b.DueOn.Valid && b.DueOn.Time.Before(todayUTC()):
		return "Overdue"
	case paid > 0:
		return "Part paid"
	default:
		return "Unpaid"
	}
}

// isSpendingAccount reports whether a CoA account_type is a spending account a bill
// may post to — the same set ListBillCategories filters the picker to.
func isSpendingAccount(accountType string) bool {
	switch accountType {
	case "COST_OF_SALES", "ADMIN_EXPENSE", "CAPITAL_ASSET":
		return true
	default:
		return false
	}
}

// validateReference trims the (required) supplier reference and rejects an empty /
// whitespace-only value (422). The handler's `required` binding already rejects a
// missing/empty field as a 400; this guards a whitespace-only one.
func validateReference(raw string) (string, error) {
	ref := strings.TrimSpace(raw)
	if ref == "" {
		return "", kernel.ErrValidation("reference is required", nil)
	}
	return ref, nil
}

// parseDate parses a required YYYY-MM-DD string into a pgtype.Date.
func parseDate(field, s string) (pgtype.Date, error) {
	t, err := time.Parse("2006-01-02", strings.TrimSpace(s))
	if err != nil {
		return pgtype.Date{}, kernel.ErrValidation(field+" must be a valid date in YYYY-MM-DD format", err)
	}
	return pgtype.Date{Time: t, Valid: true}, nil
}

// parseOptionalDate parses an optional YYYY-MM-DD pointer; nil/blank → SQL NULL.
func parseOptionalDate(field string, s *string) (pgtype.Date, error) {
	if s == nil || strings.TrimSpace(*s) == "" {
		return pgtype.Date{}, nil
	}
	return parseDate(field, *s)
}

// normaliseCurrency upper-cases the currency and defaults a blank to GBP.
func normaliseCurrency(c string) string {
	c = strings.ToUpper(strings.TrimSpace(c))
	if c == "" {
		return "GBP"
	}
	return c
}

// dateToStringPtr renders a nullable date as a YYYY-MM-DD *string; nil when NULL.
func dateToStringPtr(d pgtype.Date) *string {
	if !d.Valid {
		return nil
	}
	s := d.Time.Format("2006-01-02")
	return &s
}

// dueValueMinor returns the outstanding amount. due_value_minor is a generated
// column (read-only, nullable in Go); fall back to total − paid if not populated.
func dueValueMinor(b billsdb.Bill) int64 {
	if b.DueValueMinor.Valid {
		return b.DueValueMinor.Int64
	}
	return b.TotalValueMinor - b.PaidValueMinor
}

// vatRateDisplay renders the stored rate snapshot as a percent string ("20%"); "" when
// no rate was selected.
func vatRateDisplay(bps pgtype.Int4) string {
	if !bps.Valid {
		return ""
	}
	return money.BpsToPercent(bps.Int32)
}

// pgUUID wraps a uuid.UUID as a non-null pgtype.UUID.
func pgUUID(u uuid.UUID) pgtype.UUID {
	return pgtype.UUID{Bytes: u, Valid: true}
}

// uuidToStringPtr renders a nullable pgtype.UUID as a *string; nil when NULL.
func uuidToStringPtr(u pgtype.UUID) *string {
	if !u.Valid {
		return nil
	}
	s := uuid.UUID(u.Bytes).String()
	return &s
}

// todayUTC is midnight today in UTC — the cutoff for "overdue" (a bill due strictly
// before today is overdue; due today is not).
func todayUTC() time.Time {
	now := time.Now().UTC()
	return time.Date(now.Year(), now.Month(), now.Day(), 0, 0, 0, 0, time.UTC)
}
