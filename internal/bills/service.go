// UpdateBill fully replaces a bill's editable fields. The load + ownership + unpaid
// guards run inside one transaction to close the check-then-write race. Allowed to
// the creator or an owner/admin; only an UNPAID bill may be edited (else 409).
func (s *Service) UpdateBill(ctx context.Context, authUserID, authOrgID uuid.UUID, id string, req UpdateBillRequest) (*BillResponse, error) {
	billID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}
	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return nil, err
	}

	// Validate the request up-front (400/422s independent of DB state).
	in, err := s.vet(ctx, authOrgID, req.ContactID, req.Reference, req.DatedOn, req.DueOn, req.Currency, req.Comments, req.IsHirePurchase, req.CategoryID, req.Total, req.VATRateID, req.VATAmount, req.ProjectID)
	if err != nil {
		return nil, err
	}

	var resp *BillResponse
	err = kernel.WithTx(ctx, s.pool, func(tx pgx.Tx) error {
		qtx := s.queries.WithTx(tx)

		existing, err := qtx.GetBill(ctx, billsdb.GetBillParams{ID: billID, OrganisationID: authOrgID})
		if err != nil {
			if errors.Is(err, pgx.ErrNoRows) {
				return kernel.ErrNotFound("bill", id)
			}
			return kernel.ErrInternal(err)
		}
		if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
			return kernel.ErrForbidden("you do not have access to this bill")
		}
		if existing.PaidValueMinor != 0 {
			return kernel.ErrConflict("a bill that has payments recorded against it cannot be edited — remove the bank payment(s) first")
		}
		// Locked if the bill's CURRENT date is in a filed period, or it's being MOVED
		// into one.
		if err := s.assertNotFiled(ctx, authOrgID, existing.DatedOn, in.datedOn); err != nil {
			return err
		}

		updated, err := qtx.UpdateBill(ctx, billsdb.UpdateBillParams{
			ID:                 billID,
			OrganisationID:     authOrgID,
			ContactID:          in.contactID,
			Reference:          pgtype.Text{String: in.reference, Valid: true},
			DatedOn:            in.datedOn,
			DueOn:              in.dueOn,
			Currency:           in.currency,
			Comments:           in.comments,
			IsHirePurchase:     in.hp,
			CategoryID:         in.categoryID,
			VatRateID:          in.vat.RateID,
			VatRateBps:         in.vat.RateBps,
			NetValueMinor:      in.net,
			SalesTaxValueMinor: in.vat.ValueMinor,
			TotalValueMinor:    in.total,
			ProjectID:          in.projectID,
		})
		if err != nil {
			return kernel.ErrInternal(err)
		}
		resp = billToResponse(updated)
		return nil
	})
	if err != nil {
		return nil, err
	}
	return resp, nil
}

