// CreateBill validates the request, resolves VAT and writes the bill. The owning
// org + creator come from the token. Any active member may create a bill.
func (s *Service) CreateBill(ctx context.Context, authUserID, authOrgID uuid.UUID, req CreateBillRequest) (*BillResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	in, err := s.vet(ctx, authOrgID, req.ContactID, req.Reference, req.DatedOn, req.DueOn, req.Currency, req.Comments, req.IsHirePurchase, req.CategoryID, req.Total, req.VATRateID, req.VATAmount, req.ProjectID)
	if err != nil {
		return nil, err
	}

	// Can't create a bill dated inside an already-filed VAT period.
	if err := s.assertNotFiled(ctx, authOrgID, in.datedOn); err != nil {
		return nil, err
	}

	bill, err := s.queries.CreateBill(ctx, billsdb.CreateBillParams{
		OrganisationID:     authOrgID,
		CreatedByUserID:    authUserID,
		ContactID:          in.contactID,
		Reference:          pgtype.Text{String: in.reference, Valid: true},
		DatedOn:            in.datedOn,
		DueOn:              in.dueOn,
		Currency:           in.currency,
		Comments:           in.comments,
		IsHirePurchase:     in.hp,
		CategoryID:         in.categoryID,
		VatRateID:          in.vat.RateID,
		VatRateBps:         in.vat.RateBps,
		NetValueMinor:      in.net,
		SalesTaxValueMinor: in.vat.ValueMinor,
		TotalValueMinor:    in.total,
		ProjectID:          in.projectID,
	})
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	return billToResponse(bill), nil
}

