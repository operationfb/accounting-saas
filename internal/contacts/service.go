package contacts

// service.go
// =============================================================================
// Service — the business logic layer for contactsdb.
//
// A "contact" is a customer/supplier an organisation invoices or buys from. This
// service is the contacts equivalent of ExpenseService: it owns authorisation,
// validation, the mapping between the API shape (plain/optional strings) and the
// database shape (pgx types), and the calls into the sqlc-generated contacts
// package.
//
// It reuses the shared kernel: kernel.NullText / kernel.NullTextToPtr for nullable
// text, kernel.Int32FromPtr for the 0-PRESERVING *int32 → pgtype.Int4 (distinct
// from expenses' NULL-on-zero), kernel.AuthorizeMember / kernel.IsOrgAdmin for the
// membership+role check, kernel.NormaliseCountryCode, and the kernel.Err*
// constructors. The reverse Int4 → *int32 (nullInt32ToPtr) is kept local.
//
//   HTTP handler (server.go)
//     ↓
//   Service (this file)
//     ↓
//   *contactsdb.Queries (db/contacts — generated by sqlc)
//     ↓
//   PostgreSQL
//
// Note: contacts hold NO money, so there is no pence conversion here. The only
// numeric field, default_payment_terms_days, is a count of DAYS where 0 ("Due on
// Receipt") is distinct from NULL ("no contact-level terms") — hence the
// 0-preserving helper rather than the expenses' pgNullInt32 (which maps 0→NULL).
// =============================================================================

import (
	"context"
	"errors"
	"strings"
	"time"

	"github.com/google/uuid"
	"github.com/jackc/pgx/v5"
	"github.com/jackc/pgx/v5/pgtype"
	"github.com/jackc/pgx/v5/pgxpool"

	auth "github.com/operationfb/accounting-saas/db/auth"
	contactsdb "github.com/operationfb/accounting-saas/db/contacts"
	"github.com/operationfb/accounting-saas/internal/kernel"
)

// validChargeVAT is the set the charge_vat field is restricted to. It mirrors the
// CHECK constraint in contacts_schema.sql and the form's three dropdown options.
var validChargeVAT = map[string]bool{
	"ALWAYS":       true, // charge VAT to this contact on every invoice
	"NEVER":        true, // never charge VAT to this contact
	"SAME_COUNTRY": true, // only when the contact is in our country/VAT area
}

// Service holds the connection pool, the contacts query set, and the auth
// query set used to resolve the caller's membership/role.
type Service struct {
	pool        *pgxpool.Pool
	queries     *contactsdb.Queries
	authQueries auth.Querier
}

// NewService is the constructor, called once in main.go.
func NewService(pool *pgxpool.Pool, queries *contactsdb.Queries, authQueries auth.Querier) *Service {
	return &Service{
		pool:        pool,
		queries:     queries,
		authQueries: authQueries,
	}
}

// =============================================================================
// AUTHORIZATION
// =============================================================================

// authorize confirms the caller is an ACTIVE member of the organisation and
// returns their role. Mirrors ExpenseService.authorize — every contact operation
// runs this first so non-members / deactivated members are refused (403), and the
// role can gate who may modify a contact (see the update/delete ownership rule).
func (s *Service) authorize(ctx context.Context, userID, orgID uuid.UUID) (auth.OrganisationRole, error) {
	return kernel.AuthorizeMember(ctx, s.authQueries, userID, orgID)
}

// =============================================================================
// CREATE
// =============================================================================

// CreateContact validates + normalises the request and inserts one contact. The
// owning organisation and the creator come from the authenticated token, never
// from the body. A single-table INSERT needs no transaction.
//
// Any active member may create a contact (contacts are shared business data).
func (s *Service) CreateContact(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	req CreateContactRequest,
) (*ContactResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	countryCode, err := kernel.NormaliseCountryCode(req.CountryCode)
	if err != nil {
		return nil, err
	}

	chargeVAT, err := normaliseChargeVAT(req.ChargeVAT)
	if err != nil {
		return nil, err
	}

	// invoice_language defaults to 'en'; display_contact_name defaults to TRUE
	// (the form's checked-by-default box, so a nil pointer means "checked").
	invoiceLanguage := strings.TrimSpace(req.InvoiceLanguage)
	if invoiceLanguage == "" {
		invoiceLanguage = "en"
	}
	displayContactName := true
	if req.DisplayContactName != nil {
		displayContactName = *req.DisplayContactName
	}

	params := contactsdb.CreateContactParams{
		OrganisationID:                  authOrgID,
		CreatedByUserID:                 authUserID,
		FirstName:                       kernel.NullText(req.FirstName),
		LastName:                        kernel.NullText(req.LastName),
		OrganisationName:                kernel.NullText(req.OrganisationName),
		Email:                           kernel.NullText(req.Email),
		BillingEmail:                    kernel.NullText(req.BillingEmail),
		Telephone:                       kernel.NullText(req.Telephone),
		Mobile:                          kernel.NullText(req.Mobile),
		AddressLine1:                    kernel.NullText(req.AddressLine1),
		AddressLine2:                    kernel.NullText(req.AddressLine2),
		AddressLine3:                    kernel.NullText(req.AddressLine3),
		Town:                            kernel.NullText(req.Town),
		Region:                          kernel.NullText(req.Region),
		Postcode:                        kernel.NullText(req.Postcode),
		CountryCode:                     countryCode,
		DefaultPaymentTermsDays:         kernel.Int32FromPtr(req.DefaultPaymentTermsDays),
		UsesContactLevelEmailSettings:   req.UsesContactLevelEmailSettings,
		UsesContactLevelInvoiceSequence: req.UsesContactLevelInvoiceSequence,
		DisplayContactName:              displayContactName,
		ChargeVat:                       chargeVAT,
		VatRegistrationNumber:           kernel.NullText(req.VATRegistrationNumber),
		InvoiceLanguage:                 invoiceLanguage,
		BankSortCode:                    kernel.NullText(req.BankSortCode),
		BankAccountNumber:               kernel.NullText(req.BankAccountNumber),
		BankRecipientName:               kernel.NullText(req.BankRecipientName),
	}

	created, err := s.queries.CreateContact(ctx, params)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}
	return contactToResponse(created), nil
}

// =============================================================================
// GET
// =============================================================================

// GetContact fetches one contact by id, org-scoped. Any active member may read a
// contact (shared business data) — single read, no transaction.
func (s *Service) GetContact(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
) (*ContactResponse, error) {
	contactUUID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	row, err := s.queries.GetContact(ctx, contactsdb.GetContactParams{
		ID:             contactUUID,
		OrganisationID: authOrgID,
	})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, kernel.ErrNotFound("contact", id)
		}
		return nil, kernel.ErrInternal(err)
	}
	return contactToResponse(row), nil
}

// =============================================================================
// LIST
// =============================================================================

// ListContacts returns every active contact in the caller's organisation
// (contacts are shared, so there is no per-user filtering like expenses have).
// Any active member may list.
func (s *Service) ListContacts(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
) ([]*ContactResponse, error) {
	if _, err := s.authorize(ctx, authUserID, authOrgID); err != nil {
		return nil, err
	}

	rows, err := s.queries.ListContacts(ctx, authOrgID)
	if err != nil {
		return nil, kernel.ErrInternal(err)
	}

	out := make([]*ContactResponse, 0, len(rows))
	for _, row := range rows {
		out = append(out, contactToResponse(row))
	}
	return out, nil
}

