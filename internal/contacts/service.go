// UPDATE
// =============================================================================

// UpdateContact performs a full update of a contact's editable fields (PUT
// semantics). Authorisation: the caller must be the contact's creator, or an
// owner/admin of the organisation (mirrors the expense ownership rule). The
// fetch (for the ownership check + 404) and the update are two org-scoped,
// soft-delete-aware statements; created_by can't change, so no transaction is
// needed to avoid a race.
func (s *Service) UpdateContact(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
	req UpdateContactRequest,
) (*ContactResponse, error) {
	contactUUID, err := uuid.Parse(id)
	if err != nil {
		return nil, kernel.ErrValidation("id is not a valid UUID", err)
	}

	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return nil, err
	}

	countryCode, err := kernel.NormaliseCountryCode(req.CountryCode)
	if err != nil {
		return nil, err
	}
	chargeVAT, err := normaliseChargeVAT(req.ChargeVAT)
	if err != nil {
		return nil, err
	}
	invoiceLanguage := strings.TrimSpace(req.InvoiceLanguage)
	if invoiceLanguage == "" {
		invoiceLanguage = "en"
	}
	displayContactName := true
	if req.DisplayContactName != nil {
		displayContactName = *req.DisplayContactName
	}

	// Fetch first: 404 if it doesn't exist in this org, then enforce ownership.
	existing, err := s.queries.GetContact(ctx, contactsdb.GetContactParams{
		ID:             contactUUID,
		OrganisationID: authOrgID,
	})
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, kernel.ErrNotFound("contact", id)
		}
		return nil, kernel.ErrInternal(err)
	}
	if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
		return nil, kernel.ErrForbidden("you do not have access to this contact")
	}

	updated, err := s.queries.UpdateContact(ctx, contactsdb.UpdateContactParams{
		ID:                              contactUUID,
		OrganisationID:                  authOrgID,
		FirstName:                       kernel.NullText(req.FirstName),
		LastName:                        kernel.NullText(req.LastName),
		OrganisationName:                kernel.NullText(req.OrganisationName),
		Email:                           kernel.NullText(req.Email),
		BillingEmail:                    kernel.NullText(req.BillingEmail),
		Telephone:                       kernel.NullText(req.Telephone),
		Mobile:                          kernel.NullText(req.Mobile),
		AddressLine1:                    kernel.NullText(req.AddressLine1),
		AddressLine2:                    kernel.NullText(req.AddressLine2),
		AddressLine3:                    kernel.NullText(req.AddressLine3),
		Town:                            kernel.NullText(req.Town),
		Region:                          kernel.NullText(req.Region),
		Postcode:                        kernel.NullText(req.Postcode),
		CountryCode:                     countryCode,
		DefaultPaymentTermsDays:         kernel.Int32FromPtr(req.DefaultPaymentTermsDays),
		UsesContactLevelEmailSettings:   req.UsesContactLevelEmailSettings,
		UsesContactLevelInvoiceSequence: req.UsesContactLevelInvoiceSequence,
		DisplayContactName:              displayContactName,
		ChargeVat:                       chargeVAT,
		VatRegistrationNumber:           kernel.NullText(req.VATRegistrationNumber),
		InvoiceLanguage:                 invoiceLanguage,
		BankSortCode:                    kernel.NullText(req.BankSortCode),
		BankAccountNumber:               kernel.NullText(req.BankAccountNumber),
		BankRecipientName:               kernel.NullText(req.BankRecipientName),
	})
	if err != nil {
		// The row was live a moment ago; ErrNoRows means it was deleted in between.
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, kernel.ErrNotFound("contact", id)
		}
		return nil, kernel.ErrInternal(err)
	}
	return contactToResponse(updated), nil
}

// =============================================================================
// DELETE
// =============================================================================

// DeleteContact soft-deletes a contact (sets deleted_at) so it disappears from
// lists and reads while the row is retained for audit / referencing invoices.
// Authorisation mirrors UpdateContact: the caller must be the creator or an
// owner/admin of the organisation.
func (s *Service) DeleteContact(
	ctx context.Context,
	authUserID uuid.UUID,
	authOrgID uuid.UUID,
	id string,
) error {
	contactUUID, err := uuid.Parse(id)
	if err != nil {
		return kernel.ErrValidation("id is not a valid UUID", err)
	}

	role, err := s.authorize(ctx, authUserID, authOrgID)
	if err != nil {
		return err
	}

	existing, err := s.queries.GetContact(ctx, contactsdb.GetContactParams{
		ID:             contactUUID,
		OrganisationID: authOrgID,
	})
	if err != nil {
		// Covers an unknown id and an already-deleted contact (GetContact filters
		// deleted_at IS NULL) → 404.
		if errors.Is(err, pgx.ErrNoRows) {
			return kernel.ErrNotFound("contact", id)
		}
		return kernel.ErrInternal(err)
	}
	if existing.CreatedByUserID != authUserID && !kernel.IsOrgAdmin(role) {
		return kernel.ErrForbidden("you do not have access to this contact")
	}

	if err := s.queries.SoftDeleteContact(ctx, contactsdb.SoftDeleteContactParams{
		ID:             contactUUID,
		OrganisationID: authOrgID,
	}); err != nil {
		return kernel.ErrInternal(err)
	}
	return nil
}

// =============================================================================
// VALIDATION / NORMALISATION HELPERS
// =============================================================================

// normaliseChargeVAT defaults a blank value to 'SAME_COUNTRY' (the form's default)
// and rejects anything outside the allowed set. This is defence-in-depth behind
// the handler's `oneof` binding and the DB CHECK, so the service is correct even
// when called directly.
func normaliseChargeVAT(raw string) (string, error) {
	v := strings.TrimSpace(raw)
	if v == "" {
		return "SAME_COUNTRY", nil
	}
	if !validChargeVAT[v] {
		return "", kernel.ErrValidation("charge_vat must be one of ALWAYS, NEVER, SAME_COUNTRY", nil)
	}
	return v, nil
}

