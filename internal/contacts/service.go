// contactToResponse converts a database Contact row (pgx types, UUIDs) into the
// API response shape (plain/optional strings). Reuses kernel.NullTextToPtr from
// expense_service.go for the nullable text columns.
func contactToResponse(c contactsdb.Contact) *ContactResponse {
	return &ContactResponse{
		ID:                              c.ID.String(),
		OrganisationID:                  c.OrganisationID.String(),
		CreatedByUserID:                 c.CreatedByUserID.String(),
		FirstName:                       kernel.NullTextToPtr(c.FirstName),
		LastName:                        kernel.NullTextToPtr(c.LastName),
		OrganisationName:                kernel.NullTextToPtr(c.OrganisationName),
		Email:                           kernel.NullTextToPtr(c.Email),
		BillingEmail:                    kernel.NullTextToPtr(c.BillingEmail),
		Telephone:                       kernel.NullTextToPtr(c.Telephone),
		Mobile:                          kernel.NullTextToPtr(c.Mobile),
		AddressLine1:                    kernel.NullTextToPtr(c.AddressLine1),
		AddressLine2:                    kernel.NullTextToPtr(c.AddressLine2),
		AddressLine3:                    kernel.NullTextToPtr(c.AddressLine3),
		Town:                            kernel.NullTextToPtr(c.Town),
		Region:                          kernel.NullTextToPtr(c.Region),
		Postcode:                        kernel.NullTextToPtr(c.Postcode),
		CountryCode:                     c.CountryCode,
		DefaultPaymentTermsDays:         nullInt32ToPtr(c.DefaultPaymentTermsDays),
		UsesContactLevelEmailSettings:   c.UsesContactLevelEmailSettings,
		UsesContactLevelInvoiceSequence: c.UsesContactLevelInvoiceSequence,
		DisplayContactName:              c.DisplayContactName,
		ChargeVAT:                       c.ChargeVat,
		VATRegistrationNumber:           kernel.NullTextToPtr(c.VatRegistrationNumber),
		InvoiceLanguage:                 c.InvoiceLanguage,
		BankSortCode:                    kernel.NullTextToPtr(c.BankSortCode),
		BankAccountNumber:               kernel.NullTextToPtr(c.BankAccountNumber),
		BankRecipientName:               kernel.NullTextToPtr(c.BankRecipientName),
		IsActive:                        c.IsActive,
		CreatedAt:                       c.CreatedAt.Time.Format(time.RFC3339),
		UpdatedAt:                       c.UpdatedAt.Time.Format(time.RFC3339),
	}
}

// =============================================================================
// PGX INTEGER HELPERS (0-preserving)
//
// These complement the helpers in expense_service.go. Crucially they DIFFER from
// pgNullInt32 there: pgNullInt32 maps 0→NULL (right for vat_rate_bps, where 0
// means "no rate"), but for default_payment_terms_days a 0 means "Due on Receipt"
// and must be PRESERVED — only a nil pointer becomes NULL.
// =============================================================================

// kernel.Int32FromPtr (0-preserving *int32 → pgtype.Int4) moved to internal/kernel as
// kernel.Int32FromPtr; it stays callable here under the same name via aliases.go.

// nullInt32ToPtr is the reverse: pgtype.Int4 → *int32.
// NULL → nil; otherwise a pointer to the value (0 is kept, not dropped).
func nullInt32ToPtr(n pgtype.Int4) *int32 {
	if !n.Valid {
		return nil
	}
	v := n.Int32
	return &v
}
